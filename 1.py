#----------------------------------数据类型--------------------------------
#-----分为：整型，浮点数，布尔，字符串，列表，字典，元组，集合------------------

# int -- 整型
num1=1
# int -- 浮点数
num2=1.1

print(type(num1))
print(type(num2))

# str -- 字符串，特点：数据要带引号
a="哈哈哈哈"
print(type(a))

# bool -- 布尔， 判断使用，两个取值= True and False
b=True
print(type(b))

# list -- 列表
c=[10, 20, 30]
print(type(c))

# tuple -- 元组
d=(10, 20, 30)
print(type(d))

# set -- 集合
e={10, 20, 30}
print(type(e))

# dict -- 字典 -- 键值对
f={'name':"Tony", "age":24}
print(type(f))