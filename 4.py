#需求：
#       如果年龄小于18岁为童工，不合法
#       如果年龄18~60岁之间，为合法工作年龄
#       如果年龄大于60岁为退休年龄

age = int(input("请输入你的年龄："))
#童工
if age < 18:
    print("你输入的年龄为"f'{age},是童工，不合法')
#合法年龄
elif (age > 18) and (age < 60): # 也可以用 "18 < age < 60"
    print("你输入的年龄为"f'{age} 为合法工作年龄')
#退休年龄
else:
    print("你输入的年龄为"f'{age} 为退休年龄')
print("系统关闭！")



